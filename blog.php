					<ul type="circle" class="blog-items">
					<li>2 cups all-purpose flour</li>
					<li>1/2 teaspoon salt</li>
					<li>1/2 cup unsalted butter, cold and diced</li>
					<li>1/2 cup cold water</li>
					
				    </ul></li>
					<li>For the filling:
					<ul type="circle" class="blog-items">
					<li>1 tablespoon olive oil</li>
					<li>1 onion, finely chopped</li>
					<li>2 cloves garlic, minced</li>
					<li>1/2 pound ground beef</li>
					<li>1 teaspoon ground cumin</li>
					<li>1/2 teaspoon paprika</li>
					<li>Salt and pepper to taste</li>
					<li>1 hard-boiled egg, chopped</li>
					<li>1/4 cup green olives, chopped</li>
					<li>1/4 cup raisins (optional)</li>
					</ul></li></ul>
					<h6 class="card-title-b">Instructions:</h6>
				 
			      <ul type="disc">
				    <li>Dough:
					 <ol class="blog-items"><li>Whisk flour and salt in a bowl. Cut in cold diced butter until mixture is crumbly.</li>
					<li>Gradually add cold water, mix until dough forms. Wrap in plastic wrap, refrigerate 30 minutes.</li></ol></li>
					 <li type="disc" class="blog-items">Filling:
					<ol class="blog-items"><li>Saute chopped onion in olive oil until translucent.</li>
					<li>Add minced garlic, cook until fragrant.</li>
					<li>Add ground beef, cumin, paprika, salt, and pepper. Cook until browned.</li>
					<li>Stir in chopped hard-boiled egg, green olives, and raisins (optional). Cool.</li></ol></li>
					<li type="disc" class="blog-items">Assembly:
					<ol class="blog-items"><li>Preheat oven to 375°F (190°C), line baking sheet with parchment.</li>
					<li>Roll out chilled dough, cut into 4-5 inch rounds.</li>
					<li>Place filling in center of each round. Fold over, seal edges with fork.</li>
					<li>Arrange on baking sheet, brush with beaten egg.</li></ol></li>
					</ul>
				 
					<p class="blog-items"><b>Bake:</b> 20-25 minutes until golden brown. Serve hot with chimichurri sauce.</p>
